// Exporting a function
export function add(a: number, b: number): number {
    return a + b;
}

// Exporting a constant
export const PI: number = 3.14159;

// Exporting a class
export class Calculator {
    add(a: number, b: number): number {
        return a + b;
    }

    subtract(a: number, b: number): number {
        return a - b;
    }
}
